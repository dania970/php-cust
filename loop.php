function countChar($string, $char)
{
    $counter = 0;
    for ($i = 0; $i < strlen($string); $i++) {
        if (strtoupper(substr($string, $i, 1)) == $char) {
            $counter = $counter + 1;
        }
    }
    echo "The letter ( $char ) is repeated ( $counter ) times in this sentence : ( $string ) ";
}
